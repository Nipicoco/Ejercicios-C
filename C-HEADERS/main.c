/* 
funcion: sumatoria()
esta funcion printeara la suma de 2 numeros
*/

int sumatoria(int x, int y)
{
    int suma;
    suma = x + y;
    return suma;
}

/*
funcion: sumatoria()
esta funcion printeara el promedio de 2 numeros
*/

float promNumeros(int x, int y)
{
    int suma;
    float prom;

    suma = x + y;
    prom = suma / 2;
    return prom;
}
